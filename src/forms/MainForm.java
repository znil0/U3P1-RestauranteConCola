    // Método getAllDataFromInput()
    // Obtiene todos los valores de los JTextBox de la sección de Subir y
    // crea un objeto Pedido en base en ellos. Si hay algún valor inválido
    // muestra un mensaje de error y retorna un null.
    private Pedido getAllDataFromInput(){
        // Obtener todos los valores de los JTextBox
        String nombreCliente = jtfNombreCliente.getText();
        String idPedido = jtfIdPedido.getText();
        String nombrePlatillo = jtfNombrePlatillo.getText();
        
        int cantidad;
        try {
            cantidad = Integer.parseInt(jtfCantidad.getText());
        } catch (NumberFormatException e){
            JOptionPane.showMessageDialog(null, 
                    "Por favor, introduzca un número válido en el apartado de \"Cantidad\".", 
                    "Cantidad Inválida", 
                    JOptionPane.ERROR_MESSAGE);
            return null;
        }
        
        double precio;
        try {
            precio = Double.parseDouble(jtfPrecio.getText());
        } catch (NumberFormatException e){
            JOptionPane.showMessageDialog(null, 
                    "Por favor, introduzca un número válido en el apartado de \"Precio\".", 
                    "Precio Inválido", 
                    JOptionPane.ERROR_MESSAGE);
            return null;
        }
        
        Hora hora;
        try {
            hora = new Hora(jtfHora.getText());
        } catch (NumberFormatException e){
            JOptionPane.showMessageDialog(null, 
                    "Por favor, introduzca una hora válida en formato 24h \"16:15\" en el apartado de \"Hora\".", 
                    "Hora Inválida", 
                    JOptionPane.ERROR_MESSAGE);
            return null;
        }
        
        int prioridad;
        try {
            prioridad = Integer.parseInt(jtfTipoCliente.getText());
        } catch (NumberFormatException e){
            JOptionPane.showMessageDialog(null, 
                    "Por favor, introduzca un número válido en el apartado de \"Tipo de Cliente\".", 
                    "Prioridad Inválida", 
                    JOptionPane.ERROR_MESSAGE);
            return null;
        }
        
        // Comprobación de que todos los demás espacios están llenos
        if (nombreCliente.equals("")
                || idPedido.equals("")
                || nombrePlatillo.equals("") 
                || cantidad <= 0
                || precio <= 0
                || prioridad < 0){
            JOptionPane.showMessageDialog(null, 
                    "Por favor, llene todos los espacios requeridos con valores válidos.", 
                    "Valores Inválidos", 
                    JOptionPane.ERROR_MESSAGE);
            return null;
        }
        
        Pedido pedidoObtenido = new Pedido(nombreCliente,
        idPedido,
        nombrePlatillo,
        cantidad,
        precio,
        hora,
        prioridad);
        
        return pedidoObtenido;
    }
    
    // Método showActualPedido(){
    // Muestra los datos del pedido a completar, es decir, el último elemento
    // en la cola.
    private void showActualPedido(){
        Pedido pedidoActual = colaPedidos.front();
        
        if (pedidoActual == null){
            // Esto implica que la cola está vacía, por lo que ponemos valores
            // por default en el pedido actual.
            setDefaultActualPedido();
            return;
        }
        
        // Actualizamos valores del pedido actual.
        lbActualNombreCliente.setText(pedidoActual.getNombreCliente());
        lbActualIdPedido.setText(pedidoActual.getIdPedido());
        lbActualNombrePlatilloYCant.setText(pedidoActual.getNombrePlatillo() + " (" + pedidoActual.getCantidad() + ")");
        lbActualHora.setText(pedidoActual.getHora().getHora("HH:MM"));
        lbActualPrioridad.setText("Prioridad " + pedidoActual.getPrioridad());
        lbActualPrecio.setText("$" + pedidoActual.getPrecio());        
    }
    
    private void setDefaultActualPedido(){
        lbActualNombreCliente.setText("(nombre del cliente)");
        lbActualIdPedido.setText("(id del pedido)");
        lbActualNombrePlatilloYCant.setText("(nom del pedido + cantidad)");
        lbActualHora.setText("(hora)");
        lbActualPrioridad.setText("(prioridad)");
        lbActualPrecio.setText("$(precio)");
    }
    
