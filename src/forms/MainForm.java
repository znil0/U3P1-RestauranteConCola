    // BOTÓN ENCARGADO DE ELIMINAR UN PEDIDO AL COMPLETARSE
    private void btnPedidoCompletadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPedidoCompletadoActionPerformed
        // Eliminar pedido de la cola
        colaPedidos.eliminar();
        
        // Actualizar TextArea de la Cola
        taColaPedidos.setText(colaPedidos.mostrar());
        
        // Actualizar valores del pedido actual
        showActualPedido();
    }//GEN-LAST:event_btnPedidoCompletadoActionPerformed

    
    // BOTÓN ENCARGADO DE IMPORTAR LA COLA DE PEDIDOS DE UN ARCHIVO DE TEXTO
    private void btnImportarPedidosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnImportarPedidosActionPerformed
        // Instanciamos el parser
        PedidoParser parser = new PedidoParser();
        
        // Importamos
        ColaCircularPedidos colaPedidosImportada = parser.importFromFile();
        
        if (colaPedidosImportada != null) {
            JOptionPane.showMessageDialog(null, 
                "Se ha importado con éxito la cola de pedidos.", 
                "Cola importada", 
                JOptionPane.INFORMATION_MESSAGE);
            colaPedidos = colaPedidosImportada;
        }
        else JOptionPane.showMessageDialog(null, 
                "No fue posible exportar la cola de pedidos. \nEsto puede deberse a que no haya ningún archivo guardado o a un problema de memoria.", 
                "Error", 
                JOptionPane.ERROR_MESSAGE);
        
        // Actualizar TextArea de la Cola
        taColaPedidos.setText(colaPedidos.mostrar());
        
        // Actualizar valores del pedido actual
        showActualPedido();
    }//GEN-LAST:event_btnImportarPedidosActionPerformed

    
    // BOTÓN ENCARGADO DE EXPORTAR LA COLA DE PEDIDOS A ARCHIVO DE TEXTO
    private void btnExportarPedidosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExportarPedidosActionPerformed
        // Instanciamos el parser
        PedidoParser parser = new PedidoParser();
        
        // Exportamos
        boolean result = parser.exportToFile(colaPedidos);
        if (result) JOptionPane.showMessageDialog(null, 
                "Se ha exportado con éxito la cola de pedidos.", 
                "Cola exportada", 
                JOptionPane.INFORMATION_MESSAGE);
        else JOptionPane.showMessageDialog(null, 
                "No fue posible exportar la cola de pedidos", 
                "Error", 
                JOptionPane.ERROR_MESSAGE);
        
        // Al exportar se vacía la cola, pues no se puede acceder a los elementos
        // del medio si no se vacía antes.
        
        // Actualizar TextArea de la Cola
        taColaPedidos.setText(colaPedidos.mostrar());
        
        // Actualizar valores del pedido actual
        showActualPedido();
    }//GEN-LAST:event_btnExportarPedidosActionPerformed

    
    
    // BOTÓN ENCARGADO DE SUBIR UN PEDIDO
    private void btnSubirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubirActionPerformed
        // Crear pedido según los JTextBox
        Pedido pedidoObtenido = getAllDataFromInput();
        if (pedidoObtenido == null) return;
        
        // Insertar pedido en la cola
        colaPedidos.insertar(pedidoObtenido);
        
        // Actualizar TextArea de la Cola
        taColaPedidos.setText(colaPedidos.mostrar());
        
        // Actualizar valores del pedido actual
        showActualPedido();
    }//GEN-LAST:event_btnSubirActionPerformed

    
    
